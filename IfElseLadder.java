package Conditional;

public class IfElseLadder {
	public static void main(String[] args) {
		String name="abhi";
		if(name=="rahul") {
			System.out.println("rahul");
		}
		else if(name=="ram") {
			System.out.println("ram");
			
		}
		else {
			System.out.println(name);
		}

	}

}
