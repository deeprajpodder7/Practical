package switchCase;

public class SwitchDay {
	public static void main(String args[]) {
		
	int day=7; //declaring variable for switch
	switch(day) {
	//case statement

	case 1: System.out.println("1-Sunday");
	break;

	case 2: System.out.println("2-Monday");
	break;

	case 3:System.out.println("3-Wednesday");
	break;
	
	case 4:System.out.println("3-Wednesday");
	break;
	
	case 5:System.out.println("35-Thursday");
	break;
	
	case 6:System.out.println("6-Friday");
	break;

	case 7: System.out.println("7-Saturday");
	break;

	//default case statement
	default: System.out.println("day not valid");}
}}
